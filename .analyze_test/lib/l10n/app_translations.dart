import 'dart:ui';

import 'package:get/get.dart';

class AppTranslations extends Translations {
  static const Locale fallback = Locale('en', 'US');

  static Locale resolveLocale(Locale? deviceLocale) {
    switch (deviceLocale?.languageCode) {
      case 'ko':
        return const Locale('ko', 'KR');
      case 'ja':
        return const Locale('ja', 'JP');
      case 'zh':
        return const Locale('zh', 'CN');
      case 'hi':
        return const Locale('hi', 'IN');
    }

    return fallback;
  }

  static String rank(int rank) {
    switch (Get.locale?.languageCode) {
      case 'ko':
        return '$rank등';
      case 'ja':
        return '$rank位';
      case 'zh':
        return '第$rank名';
      case 'hi':
        return '#$rank';
    }

    return '#$rank';
  }

  static String seconds(num seconds) {
    switch (Get.locale?.languageCode) {
      case 'ko':
        return '${seconds.ceil()}초';
      case 'ja':
      case 'zh':
        return '${seconds.ceil()}秒';
      case 'hi':
        return '${seconds.ceil()} सेकंड';
    }

    return '${seconds.ceil()}s';
  }

  static String points(Object score) {
    switch (Get.locale?.languageCode) {
      case 'ko':
        return '$score점';
      case 'ja':
        return '$score点';
      case 'zh':
        return '$score分';
      case 'hi':
        return '$score अंक';
    }

    return '$score pts';
  }

  static String step(int step) {
    switch (Get.locale?.languageCode) {
      case 'ko':
        return '$step단계';
      case 'ja':
        return 'ステップ$step';
      case 'zh':
        return '步骤 $step';
      case 'hi':
        return 'चरण $step';
    }

    return 'STEP $step';
  }

  static String resetTimeLabel({
    required int days,
    required int hours,
    required int minutes,
  }) {
    switch (Get.locale?.languageCode) {
      case 'ko':
        if (days > 0) return '종료까지 $days일 $hours시간 $minutes분';
        if (hours > 0) return '종료까지 $hours시간 $minutes분';
        return '종료까지 $minutes분';
      case 'ja':
        if (days > 0) return '終了まで $days日 $hours時間 $minutes分';
        if (hours > 0) return '終了まで $hours時間 $minutes分';
        return '終了まで $minutes分';
      case 'zh':
        if (days > 0) return '距离结束 $days天 $hours小时 $minutes分';
        if (hours > 0) return '距离结束 $hours小时 $minutes分';
        return '距离结束 $minutes分';
      case 'hi':
        if (days > 0) return '$days दिन $hours घंटे $minutes मिनट बाकी';
        if (hours > 0) return '$hours घंटे $minutes मिनट बाकी';
        return '$minutes मिनट बाकी';
    }

    if (days > 0) return '${days}D ${hours}H ${minutes}M LEFT';
    if (hours > 0) return '${hours}H ${minutes}M LEFT';
    return '${minutes}M LEFT';
  }

